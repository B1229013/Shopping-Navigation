/** Returns a square Unsplash icon URL for the given food name, or null if no keyword matches. */
fun getFoodIconUrl(foodName: String): String? {
    if (foodName.isBlank()) return null
    val name = foodName.lowercase()
    return foodIconMap.entries.firstOrNull { (key, _) ->
        name.contains(key.lowercase())
    }?.value
}
